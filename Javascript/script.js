// Unary Operators Example

let a = 5;
let b = 4;

console.log("a =", a, "b =", b);

console.log("a =", a++);  // Post-increment  --5
console.log("a =", ++a);  // Pre-increment   --7

console.log("b =", b--);  // Post-decrement  --4 
console.log("b =", --b);  // Pre-decrement   --2
